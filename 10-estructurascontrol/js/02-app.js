const puntaje = 1000;

if (puntaje != 1000) {
  // operdor diferente !=
  console.log("Si es diferente!!!");
} else {
  console.log("No es diferente!!!");
}

if (puntaje === "1000") {
  // operador estricto en JS ===
  console.log("Si es igual!!!");
} else {
  console.log("No es igual!!!");
}

if (puntaje !== 1000) {
  // operdor diferente estricto !==
  // operdor diferente !=
  console.log("Si es diferente!!!");
} else {
  console.log("No es diferente!!!");
}
