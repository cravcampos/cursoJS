// el tipo de ddato boolean solo puede tener dos valores true o false

const boolean1 = true;
const boolean2 = false;

console.log(boolean1);
console.log(boolean2);

// Verificar el tipo de dato
console.log(typeof boolean2);

// Tambien se puede crear boolean con la palabra new
