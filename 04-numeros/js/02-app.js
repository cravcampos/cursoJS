// Resta -
resultado = numero - numero1;
console.log(resultado);

// Multiplicaci√≥n *
resultado = numero * numero1;
console.log(resultado);

// Division /
resultado = numero / numero1;
console.log(resultado);

// Modulo %
resultado = numero % numero1;
console.log(resultado);
